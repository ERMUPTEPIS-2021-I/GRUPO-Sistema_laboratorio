package Entidad;

/**
 *
 * @author Mathius
 */
public class ClsEProducto {

    int idProducto;
    String descripcion;
    double cantidad;
    int stock;
    int idMarca;
    int idTipo_producto;
    int idTipo_medida;
    
    public int getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(int idProducto) {
        this.idProducto = idProducto;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public double getCantidad() {
        return cantidad;
    }

    public void setCantidad(double cantidad) {
        this.cantidad = cantidad;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public int getIdMarca() {
        return idMarca;
    }

    public void setIdMarca(int idMarca) {
        this.idMarca = idMarca;
    }

    public int getIdTipo_producto() {
        return idTipo_producto;
    }

    public void setIdTipo_producto(int idTipo_producto) {
        this.idTipo_producto = idTipo_producto;
    }

    public int getIdTipo_medida() {
        return idTipo_medida;
    }

    public void setIdTipo_medida(int idTipo_medida) {
        this.idTipo_medida = idTipo_medida;
    }

    
    
    
    
    
}
